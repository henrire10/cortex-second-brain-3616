export const normalizeExerciseName = (name: string): string => {
  return name
    .trim()
    .toLowerCase()
    .replace(/[√°√†√¢√£√§]/g, 'a')
    .replace(/[√©√®√™√´]/g, 'e')
    .replace(/[√≠√¨√Æ√Ø]/g, 'i')
    .replace(/[√≥√≤√¥√µ√∂]/g, 'o')
    .replace(/[√∫√π√ª√º]/g, 'u')
    .replace(/[√ß]/g, 'c')
    .replace(/[√±]/g, 'n')
    .replace(/¬∞/g, ' graus')
    .replace(/¬∫/g, ' graus')
    .replace(/\s+/g, ' ');
};

